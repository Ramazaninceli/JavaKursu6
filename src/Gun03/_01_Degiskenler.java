package Gun03;

public class _01_Degiskenler {

    public static void main(String[] args) {

        int toplam;
        int sayac=0;

        sayac=sayac+2;
        System.out.println("sayaç =" +sayac);

        System.out.println("sayac = " + sayac);// soutv seçilirse değişken otomatik ekleniyor

        toplam=0;
        toplam=1;// en son değer her zaman geçerlidir


        System.out.println("toplam = " + toplam);

        System.out.println();//sadece alt satıra geçer
        System.out.println("Merhaba"); // sabit yazıyı ekrana yazar
        System.out.println(sayac); // sayac değerini ekrana yazar
        System.out.println("toplam = " + toplam);// toplam değerini ekrana tanımlı olarak yazdı.


    }
}
