public class _02_Ornek1 {

    public static void main(String[] args) {
        // Bir karenin kenar uzunluğu 5 tir. kareninin çevresini ve alanını hesaplayıp yazdırınız
        // çevre = kenar*4
        // alan = kenar*kenar


        int kenar=5;// değişken tanımladık ve değer atadık.

        int cevre=kenar*4;// değişken tanımladık ve değer atadık formülle
        int alan=kenar*kenar;// değişken tanımladık ve değer atadık formülle

        System.out.println("cevre = " + cevre);
        System.out.println("alan = " + alan);


    }
}
