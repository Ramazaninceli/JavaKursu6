package Gun08;

public class _06_JavaAssigmentOperators {
    public static void main(String[] args) {

        int a= 10; // a ya 10 değeri atandı

        a=5; // a ya 5 değeri atanmış olur
        a= a+6; // a ya 6 değerini ekle demek
        a+=6; // a ya 6 değeri eklendi
        // a nın artış değeri 6 ya eşittir.

        a=a+1;// a yı 1 arttırır.
        a++;  // a yı 1 arttırıyor
        ++a;  // a yı 1 arttırır
        a+=1; // a yı 1 arttırır

        /*************************/

        a=a-1; // a yı 1 azalt demek
        a--;// a yı 1 azalt demek
        --a; // a yı 1 azalt demek
        a-=1;// a yı 1 azalt demek
        a-=25; // a yı 25 azalt demek a=a-25;

        /*********************************/

        int b=10;
       b=b/5; //b yi 5 e böler
        b/=5; // b yi 5 e böler

        System.out.println("b = " + b);

        /*******************************/

        int c = 10;
        c=c*5; // c iyi 5 ile çar 50
        c*=5; // c yi 5 ile çarp 50

        System.out.println("c = " + c);







    }
}
